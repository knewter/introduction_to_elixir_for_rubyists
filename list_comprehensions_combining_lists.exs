for x <- [1, 2, 3], y <- [4, 5, 6], do: x*y
#=> [4, 5, 6, 8, 10, 12, 12, 15, 18]

for x <- [1, 2, 3], y <- [4, 5, 6], do: {x, y}
#=> [{1, 4}, {1, 5}, {1, 6}, {2, 4}, {2, 5}, {2, 6}, {3, 4}, {3, 5}, {3, 6}]

for x <- [1, 3, 5, 7, 9],
    y <- [2, 4, 6, 8, 10],
    rem((x*y)-1, 9) == 0,
    do: [x, y]
#=> [[1, 10], [5, 2], [7, 4]]
